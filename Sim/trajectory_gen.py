    #     ix,iy,ivx,ivy=linear_interp(x,y,vx,vy)
    #     traj[i]['x']=ix
    #     traj[i]['y']=iy
    #     traj[i]['vx']=ivx
    #     traj[i]['vy']=ivy
    # Saving the dictionary as a .json file.
    s_name=file_name.split('.')[0]+'.json'
    with open(s_name,'w') as fd:
        json.dump(traj,fd)
    print('{} Saved..'.format(s_name))
    print('_____________________________________')

